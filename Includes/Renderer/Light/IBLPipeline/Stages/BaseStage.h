#ifndef PABLO_RENDERER_BASESTAGE_H
#define PABLO_RENDERER_BASESTAGE_H

#include "Renderer/Utils/FrameBuffer/FrameBuffer.h"
#include "Renderer/Utils/Texture/TextureBase.h"
class BaseStage {
protected:
    explicit BaseStage(int width, int height, Shader* shader , Geometry* geometry);
    FrameBuffer* frameBuffer;
    TextureBase rendenderedResult;
    Shader* shader;
    Geometry* geometry;
    virtual TextureBase execute(TextureBase input){};

    glm::mat4 captureProjection = glm::perspective(glm::radians(90.0f), 1.0f, 0.1f, 10.0f);
    glm::mat4 captureViews[] = {
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(1.0f,  0.0f,  0.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(-1.0f,  0.0f,  0.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  1.0f,  0.0f), glm::vec3(0.0f,  0.0f,  1.0f)),
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f,  0.0f), glm::vec3(0.0f,  0.0f, -1.0f)),
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  0.0f,  1.0f), glm::vec3(0.0f, -1.0f,  0.0f)),
                    glm::lookAt(glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f,  0.0f, -1.0f), glm::vec3(0.0f, -1.0f,  0.0f))
            };
};


#endif //PABLO_RENDERER_BASESTAGE_H
